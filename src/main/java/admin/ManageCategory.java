    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jTextField2.getText());
        String cname = jTextField3.getText();
        String desc = jTextField4.getText();
        if (isEmpty()) {
            if (!categoryDao.isCategoryNameExist(cname)) {
                categoryDao.insert(id, cname, desc);
                jTable.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Description"}));
                categoryDao.getCategoriesValue(jTable, "");
                Reset();
            } else {
                JOptionPane.showMessageDialog(this, "Sản phẩm đã tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

