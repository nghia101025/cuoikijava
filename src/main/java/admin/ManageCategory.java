    private void Reset() {
        jTextField2.setText(String.valueOf(categoryDao.getMaxRow()));
        jTextField3.setText("");
        jTextField4.setText("");
        jTable.clearSelection();
        statistics.admin();
    }

    private boolean check() {
        String newCName = jTextField3.getText();

        String oldCName = model.getValueAt(rowIndex, 2).toString();
        if (newCName.equals(oldCName)) {
            return false;
        } else {
            if (!newCName.equals(oldCName)) {
                boolean x = categoryDao.isCategoryNameExist(newCName);
                if (x) {
                    JOptionPane.showMessageDialog(null, "Danh mục đã tồn tại");
                }
                return x;
            }

        }
        return false;
    }

